package com.xinzhu.xuezhibao.bean;

public class VideoFolder {
    String videoTypeId;
    String videoTypeName;
    String videoTypeImage;
    String videoTypeMoney;
    String videoTypeNum;

    public VideoFolder() {
    }

    public String getVideoTypeId() {
        return videoTypeId;
    }

    public void setVideoTypeId(String videoTypeId) {
        this.videoTypeId = videoTypeId;
    }

    public String getVideoTypeName() {
        return videoTypeName;
    }

    public void setVideoTypeName(String videoTypeName) {
        this.videoTypeName = videoTypeName;
    }

    public String getVideoTypeImage() {
        return videoTypeImage;
    }

    public void setVideoTypeImage(String videoTypeImage) {
        this.videoTypeImage = videoTypeImage;
    }

    public String getVideoTypeMoney() {
        return videoTypeMoney;
    }

    public void setVideoTypeMoney(String videoTypeMoney) {
        this.videoTypeMoney = videoTypeMoney;
    }

    public String getVideoTypeNum() {
        return videoTypeNum;
    }

    public void setVideoTypeNum(String videoTypeNum) {
        this.videoTypeNum = videoTypeNum;
    }
}
